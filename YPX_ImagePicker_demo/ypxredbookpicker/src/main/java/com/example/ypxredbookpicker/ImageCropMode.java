package com.example.ypxredbookpicker;

/**
 * Description: 图片剪裁模式
 * <p>
 * Author: peixing.yang
 * Date: 2019/2/21
 */
public class ImageCropMode {
    /**
     * 填充模式，按照图片宽度填充到容器(屏幕)宽度
     */
    public static int FILL = -1;
    /**
     * 自适应模式，按照图片高度自适应容器高度
     */
    public static int FIT = -2;
    /**
     * 正方形图片，不限制图片模式，
     */
    public static int SQUARE = -3;
}
